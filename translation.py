def get_text(lang, key, default="", **kwargs):
    """key 문자열 반환. {n}, {msg} 등은 kwargs로 치환."""
    s = LANG_DICT["KO"].get(key, default or LANG_DICT["KO"].get(key, ""))
    if isinstance(s, str) and kwargs:
        try:
            return s.format(**kwargs)
        except (KeyError, ValueError):
            return s
    return s

