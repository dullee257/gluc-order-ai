# 단일 진입점: 언어 추가 시 여기에 매핑만 추가
LANG_DICT = {
    "KO": _ko(),
    "EN": _en(),
    "ZH": _zh(),
    "JA": _ja(),
    "HI": _hi(),
}

def get_text(lang, key, default="", **kwargs):
    """lang에 해당하는 key 문자열 반환. {n}, {msg} 등은 kwargs로 치환."""
    lang = lang or "KO"
    d = LANG_DICT.get(lang, LANG_DICT["KO"])
    s = d.get(key, default or LANG_DICT["KO"].get(key, ""))
    if isinstance(s, str) and kwargs:
        try:
            return s.format(**kwargs)
        except (KeyError, ValueError):
            return s
    return s
